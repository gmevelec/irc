#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		s1[i] = s2[i];
		if (s2[i] == '\0')
		{
			while (i < n)
			{
				s1[i] = '\0';
				i++;
			}
		}
		i++;
	}
	return (s1);
}
